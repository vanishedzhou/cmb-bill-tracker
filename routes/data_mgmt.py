def _mark_refund(db, refund_trans):
    """Find the original transaction matching this refund and mark it as refunded.

    Match by: same card, same merchant, same amount, transaction date <= refund date.
    Picks the most recent un-refunded match.
    """
    row = db.execute(
        """SELECT id FROM transactions
           WHERE card_last4 = ? AND merchant = ? AND amount = ?
                 AND refunded = 0 AND trans_date <= ?
           ORDER BY trans_date DESC
           LIMIT 1""",
        (
            refund_trans["card_last4"],
            refund_trans["merchant"],
            refund_trans["amount"],
            refund_trans["trans_date"],
        ),
    ).fetchone()

    if row:
        db.execute(
            "UPDATE transactions SET refunded = 1, refund_date = ? WHERE id = ?",
            (refund_trans["trans_date"], row["id"]),
        )
        db.commit()


def _run_fetch_in_thread(fetch_log_id, date_from, date_to, fetch_type):
    """Run fetch pipeline in a background thread."""
    try:
        run_fetch_pipeline(date_from, date_to, fetch_type)
    finally:
        _running_tasks.pop(fetch_log_id, None)


@bp.route("/api/fetch", methods=["POST"])
def trigger_fetch():
    """Trigger a fetch operation.

    JSON body:
        period: '7d', '30d', '6m', '1y', '3y', '5y', 'custom'
        date_from: (for custom) YYYY-MM-DD
        date_to: (for custom) YYYY-MM-DD
    """
    data = request.get_json() or {}
    period = data.get("period", "7d")
    now = datetime.now()

    period_map = {
        "7d": timedelta(days=7),
        "30d": timedelta(days=30),
        "6m": timedelta(days=180),
        "1y": timedelta(days=365),
        "3y": timedelta(days=365 * 3),
        "5y": timedelta(days=365 * 5),
    }

    if period == "custom":
        date_from = datetime.strptime(data["date_from"], "%Y-%m-%d")
        date_to = datetime.strptime(data["date_to"], "%Y-%m-%d")
        fetch_type = "custom"
    elif period in period_map:
        date_from = now - period_map[period]
        date_to = now
        fetch_type = f"init_{period}"
    else:
        return jsonify({"error": "Invalid period"}), 400

    # Create fetch log first
    db = get_db()
    db.execute(
        "INSERT INTO fetch_logs (fetch_type, date_from, date_to, status) VALUES (?, ?, ?, 'running')",
        (fetch_type, date_from.strftime("%Y-%m-%d"), date_to.strftime("%Y-%m-%d")),
    )
    db.commit()
    fetch_log_id = db.execute("SELECT last_insert_rowid()").fetchone()[0]
    db.close()

    # Run in background thread
    thread = threading.Thread(
        target=_run_fetch_in_background,
        args=(fetch_log_id, date_from, date_to, fetch_type),
    )
    thread.daemon = True
    thread.start()
    _running_tasks[fetch_log_id] = thread

    return jsonify({"fetch_id": fetch_log_id, "status": "running"})


def _run_fetch_in_background(fetch_log_id, date_from, date_to, fetch_type):
    """Background fetch — updates the existing fetch_log entry."""
    from services.email_fetcher import fetch_cmb_emails
    from services.bill_parser import parse_email
    from services.dedup import compute_dedup_hash, is_duplicate
    from services.category_mapper import categorize_merchant

    db = get_db()
    emails_parsed = 0
    records_created = 0

    try:
        emails = fetch_cmb_emails(date_from, date_to, fetch_log_id)

        db.execute(
            "UPDATE fetch_logs SET emails_found = ? WHERE id = ?",
            (len(emails), fetch_log_id),
        )
        db.commit()

        for email_data in emails:
            uid = email_data["uid"]

            db.execute(
                """INSERT OR IGNORE INTO email_logs (email_uid, subject, from_addr, date, parsed_status)
                   VALUES (?, ?, ?, ?, 'pending')""",
                (uid, email_data["subject"], email_data["from_addr"], email_data["date"]),
            )
            db.commit()

            try:
                result = parse_email(email_data)
                bill_type = result["bill_type"]
                transactions = result["transactions"]

                if bill_type == "unknown" or not transactions:
                    db.execute(
                        "UPDATE email_logs SET parsed_status = 'skipped', bill_type = ? WHERE email_uid = ?",
                        (bill_type, uid),
                    )
                    db.commit()
                    continue

                email_records = 0
                for trans in transactions:
                    # Handle refunds: find and mark original transaction
                    if trans.get("is_refund"):
                        _mark_refund(db, trans)
                        continue

                    dedup_hash = compute_dedup_hash(
                        trans["card_last4"],
                        trans["trans_date"],
                        trans["amount"],
                        trans["merchant"],
                    )

                    if is_duplicate(db, dedup_hash):
                        continue

                    category = categorize_merchant(trans["merchant"])

                    db.execute(
                        """INSERT INTO transactions
                           (card_last4, trans_date, post_date, amount, currency, merchant,
                            category, bill_type, dedup_hash, email_uid)
                           VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?)""",
                        (
                            trans["card_last4"],
                            trans["trans_date"],
                            trans.get("post_date", trans["trans_date"]),
                            trans["amount"],
                            trans.get("currency", "CNY"),
                            trans["merchant"],
                            category,
                            trans["bill_type"],
                            dedup_hash,
                            uid,
                        ),
                    )
                    email_records += 1
                    records_created += 1

                db.execute(
                    """UPDATE email_logs
                       SET parsed_status = 'success', bill_type = ?, records_count = ?
                       WHERE email_uid = ?""",
                    (bill_type, email_records, uid),
                )
                db.commit()
                emails_parsed += 1

            except Exception as e:
                db.execute(
                    "UPDATE email_logs SET parsed_status = 'failed', error_message = ? WHERE email_uid = ?",
                    (str(e), uid),
                )
                db.commit()

        db.execute(
            """UPDATE fetch_logs
               SET status = 'completed', emails_parsed = ?, records_created = ?,
                   completed_at = datetime('now', 'localtime')
               WHERE id = ?""",
            (emails_parsed, records_created, fetch_log_id),
        )
        db.commit()

    except Exception as e:
        db.execute(
            """UPDATE fetch_logs
               SET status = 'failed', error_message = ?,
                   completed_at = datetime('now', 'localtime')
               WHERE id = ?""",
            (str(e), fetch_log_id),
        )
        db.commit()
    finally:
        db.close()
        _running_tasks.pop(fetch_log_id, None)


