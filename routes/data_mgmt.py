@bp.route("/api/merchants/category", methods=["PUT"])
def update_merchant_category():
    """Batch update category for all transactions of a merchant.

    Also adds/updates a category_rule for future auto-categorization.
    JSON body: { merchant: "xxx", category: "xxx" }
    """
    data = request.get_json() or {}
    merchant = data.get("merchant", "").strip()
    category = data.get("category", "").strip()
    if not merchant or not category:
        return jsonify({"error": "merchant and category are required"}), 400

    db = get_db()
    try:
        result = db.execute(
            "UPDATE transactions SET category = ? WHERE merchant = ?",
            (category, merchant),
        )
        updated = result.rowcount
        db.commit()

        # Upsert category rule
        existing = db.execute(
            "SELECT id FROM category_rules WHERE pattern = ?", (merchant,)
        ).fetchone()
        if existing:
            db.execute(
                "UPDATE category_rules SET category = ? WHERE id = ?",
                (category, existing["id"]),
            )
        else:
            db.execute(
                "INSERT INTO category_rules (pattern, category, priority) VALUES (?, ?, 20)",
                (merchant, category),
            )
        db.commit()

        return jsonify({"ok": True, "updated": updated})
    finally:
        db.close()
