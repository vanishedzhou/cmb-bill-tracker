@bp.route("/api/stats")
def data_stats():
    """Get overall data statistics."""
    db = get_db()
    try:
        total_trans = db.execute("SELECT COUNT(*) as cnt FROM transactions").fetchone()["cnt"]
        total_amount = db.execute(
            "SELECT COALESCE(SUM(amount), 0) as total FROM transactions"
        ).fetchone()["total"]

        cards = db.execute(
            """SELECT card_last4, COUNT(*) as cnt, SUM(amount) as total
               FROM transactions GROUP BY card_last4"""
        ).fetchall()

        date_range = db.execute(
            "SELECT MIN(trans_date) as min_date, MAX(trans_date) as max_date FROM transactions"
        ).fetchone()

        email_stats = db.execute(
            """SELECT parsed_status, COUNT(*) as cnt
               FROM email_logs GROUP BY parsed_status"""
        ).fetchall()

        return jsonify(
            {
                "total_transactions": total_trans,
                "total_amount": round(total_amount, 2),
                "cards": [
                    {"card_last4": c["card_last4"], "count": c["cnt"], "total": round(c["total"], 2)}
                    for c in cards
                ],
                "date_range": {
                    "from": date_range["min_date"],
                    "to": date_range["max_date"],
                }
                if date_range["min_date"]
                else None,
                "email_stats": {r["parsed_status"]: r["cnt"] for r in email_stats},
            }
        )
    finally:
        db.close()


